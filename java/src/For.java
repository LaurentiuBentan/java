public class For {
    public static void main(String[] args) {
        for (int dalmatieni = 1; dalmatieni<=101; dalmatieni++) {
            System.out.println("dalmatianul cu numarul: "+ dalmatieni);

        }
        for (int dalmatieni = 1; dalmatieni<=101; dalmatieni=dalmatieni+2) {
            System.out.println("dalmatianul cu numarul: " + dalmatieni);
        }
    }

}
